print "How old are you? "
age = gets.chomp
print "How tall are you? "
height = gets.chomp
print "How much do you weigh? "
weight = gets.chomp

puts "So you're #{age} old, #{height} tall and #{weight} heavy."

=begin
1. gets.chomp asks for input from the user
2. you can also use gets.chomp.to_i to ask for integers and gets.chomp.to_f for a float

=end
